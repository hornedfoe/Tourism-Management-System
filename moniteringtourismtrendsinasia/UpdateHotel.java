    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(getId.getText());       
        String newName = getNewName.getText();
        String newPrice = getNewPrice.getText();
        boolean flag = false;
        String query = "select id from hotel";
        java.sql.PreparedStatement p;
        ResultSet rs;
        java.sql.Connection con;
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/miniproject" , "root","Ranjith123");
            p = con.prepareStatement(query);
            rs = p.executeQuery(query);
            while(rs.next()){
                if(rs.getInt("id") == id){
                    flag = true;
                    break;
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        if(flag && (!newName.equals("") || !newPrice.equals(""))){
            try{
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/miniproject" , "root","Ranjith123");
                if(newName.equals("")){
                    query = "update hotel set price = " + newPrice + " where id = " + id;
                }else if(newPrice.equals("")){
                    query = "update hotel set name = '" + newName + "' where id = " + id;
                }else{
                    query = "update hotel set name = '" + newName +"' , price =  "+ newPrice +" where id = " + id;
                }
                p = con.prepareStatement(query);
                p.executeUpdate(query);
                JOptionPane.showMessageDialog(null,"Updated Successfully");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Enter a valid input");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

